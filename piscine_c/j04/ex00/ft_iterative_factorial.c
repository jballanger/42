int	ft_iterative_factorial(int nb)
{
	int	i;

	i = 1;
	if (nb >= 0 && nb < 13)
	{
		while (nb > 0)
		{
			i *= nb;
			nb--;
		}
	}
	else
	{
		return (0);
	}
	return (i);
}
