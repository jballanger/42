int		check_error(char **argv)
{
	if (argv[2][0] == '+')
		return (0);
	if (argv[2][0] == '-')
		return (0);
	if (argv[2][0] == '*')
		return (0);
	if (argv[2][0] == '/')
		return (0);
	if (argv[2][0] == '%')
		return (0);
	return (1);
}
