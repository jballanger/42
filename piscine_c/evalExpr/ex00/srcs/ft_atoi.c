int		ft_atoi(char *str)
{
	int		i;
	int		sign;
	int		nbr;

	i = 0;
	sign = 0;
	nbr = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] )
		nbr *= 10;
		nbr += (str[i] - 48);
		i++;
	}
	return (nbr);
}
